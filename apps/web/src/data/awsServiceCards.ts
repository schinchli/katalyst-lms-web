export const SERVICE_CATEGORIES: ServiceCategory[] = [
  'Generative AI',
  'ML Platform (SageMaker)',
  'AI Services',
  'Data & Analytics',
  'Security & Governance',
  'Compute & Cost',
];

export function getServiceCard(id: string): AwsServiceCard | undefined {
  return AWS_SERVICE_CARDS.find((c) => c.id === id);
}

/** Services a given learning path expects you to know. */
export function servicesForPath(pathId: string): AwsServiceCard[] {
  return AWS_SERVICE_CARDS.filter((c) => c.paths.includes(pathId));
}

/** Path ids referenced by at least one card (drives the path selector). */
export function pathsWithServices(): string[] {
  return [...new Set(AWS_SERVICE_CARDS.flatMap((c) => c.paths))];
}
